package pkgswitch;

public class Switch {

    public static void main(String[] args) {
        int day = 6;
        String dayString;
        switch (day) {
            case 1:
                dayString = "Sunday";
                System.out.println(dayString);
                break;
            case 2:
                dayString = "monday";
                System.out.println(dayString);
                break;
            case 3:
                dayString = "tuesday";

                System.out.println(dayString);
                break;
            case 4:
                dayString = "wednesday";
                System.out.println(dayString);
                break;
            case 5:
                dayString = "thrusday";
                System.out.println(dayString);
                break;
            case 6:
                dayString = "friday";
                System.out.println(dayString);
                break;
        }
    }
}
