package ifloop;

public class IfLoop {

    public static void main(String[] args) {
        int mark = 60;
        char grade;
        if (mark >= 90) {
            System.out.println("grade=A");
        } else if (mark >= 80) {
            System.out.println("grade=B");
        } else if (mark >= 70) {
            System.out.println("grade=C");
        } else if (mark >= 60) {
            System.out.println("grade=D");
        }

    }
}
